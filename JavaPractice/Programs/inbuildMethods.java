package Programs;

public class inbuildMethods {
	public static void main(String[] args) {
		
		System.out.println("Inbuild methods in Java:");
		
		System.out.println(Math.PI);//            3.14
		System.out.println(Math.pow(2, 3));//     8.0
		System.out.println(Math.sqrt(25));//      5.0
		System.out.println(Math.max(7, 4));//     7
		System.out.println(Math.ceil(7.5));//     8.0
		System.out.println(Math.floor(7.4));//    7.0
		System.out.println(Math.exp(7));//     1096.63
		System.out.println(Math.log(24));//       3.17
		System.out.println(Math.sin(10));//      -0.54
	}

}
